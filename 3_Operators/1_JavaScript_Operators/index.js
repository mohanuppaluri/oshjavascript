//Types of Operators in Java Script
// Arithmetic
// Assignment
// Comparison
// Logical
// Bitwise


let x = 10;
let y = 2;


console.log(x+y);
console.log(x-y);
console.log(x*y);
console.log(x/y);
console.log(x%y);
console.log(x**y); // Exponential Operator


// Increment ( ++ )
console.log(++x)
console.log(x)
console.log(x++)

// Decrement ( -- )
console.log(--x)
console.log(x)
console.log(x--)



